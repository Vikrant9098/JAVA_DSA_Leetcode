class Solution {
    public int[] productExceptSelf(int[] nums) 
    {
        int n = nums.length; // संख्यांच्या अ‍ॅरेची लांबी मिळवा

        int[] result = new int[n]; // अंतिम उत्तर संग्रहित करण्यासाठी नवीन अ‍ॅरे तयार करा

        // **स्टेप 1: डावीकडील गुणाकार साठवा (Left Pass)**
        result[0] = 1; // पहिल्या घटकासाठी डावीकडून कोणताही घटक नाही, त्यामुळे 1 असतो
        for (int i = 1; i < n; i++) 
        {
            result[i] = result[i - 1] * nums[i - 1]; // डावीकडील सर्व संख्यांचा गुणाकार साठवा
        }

        // **स्टेप 2: उजवीकडील गुणाकार साठवा आणि अंतिम गुणाकार मिळवा (Right Pass)**
        int rightProduct = 1; // उजव्या बाजूचा प्रारंभिक गुणाकार 1 आहे
        for (int i = n - 1; i >= 0; i--) 
        {  
            // उजव्या बाजूने फिरा
            result[i] *= rightProduct; // डावीकडील गुणाकार * उजव्या बाजूचा गुणाकार
            rightProduct *= nums[i]; // पुढच्या उजव्या घटकासाठी अपडेट करा
        }

        return result; // अंतिम उत्तर परत करा
    }
}
